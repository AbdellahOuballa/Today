    void getuserdiaries(String SD){
        try {
            //SD = SD.split(" ")[0];
            //System.out.println(SD);
            String req = "select id,Title,Text,Mood,City,username from diary where username ='"+username+"' and dated = to_date('"+SD+"','dd-mm-yyyy')";
            System.err.println(req);
            Statement st = conn.createStatement();
            ResultSet r = st.executeQuery(req);
            while(r.next()){
                Model.addRow(new Object[]{r.getObject(1),r.getObject(2),r.getObject(3),r.getObject(4),r.getObject(5),r.getObject(6)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(History.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

