    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int row = jTable1.getSelectedRow();
            String sId =  (String) Model.getValueAt(row,0);
            System.out.println(sId);
            JasperDesign jasperDesign = JRXmlLoader.load("DIARY.jrxml");
            
            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
             
            Map parameters = new HashMap();
            parameters.put("PID",Integer.parseInt(sId));
//Execution du rapport
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parameters, Connexion.Seconnecter());
            // - Cr√©ation du rapport au format PDF
            JasperExportManager.exportReportToPdfFile(jasperPrint, "DIARY.pdf");
            Desktop desktop = Desktop.getDesktop();
           
            desktop.open(new File("DIARY.pdf"));
        } catch (JRException ex) {
            Logger.getLogger(History.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(History.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

